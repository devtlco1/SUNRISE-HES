from __future__ import annotations

from app.runtime.contracts import RuntimeExecutionGuardResult


def load_runtime_execution_guard(
    execution_metadata: dict[str, object] | None,
) -> RuntimeExecutionGuardResult | None:
    if not isinstance(execution_metadata, dict):
        return None
    payload = execution_metadata.get("runtime_execution_guard")
    if not isinstance(payload, dict):
        return None
    return RuntimeExecutionGuardResult.model_validate(payload)


def merge_runtime_metadata(
    existing: dict[str, object] | None,
    extra: dict[str, object] | None,
) -> dict[str, object]:
    merged: dict[str, object] = {}
    if isinstance(existing, dict):
        merged.update(existing)
    if isinstance(extra, dict):
        for key, value in extra.items():
            if isinstance(value, dict) and isinstance(merged.get(key), dict):
                merged[key] = {**merged[key], **value}  # type: ignore[index]
            else:
                merged[key] = value
    return merged
