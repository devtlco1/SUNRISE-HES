def test_redis_worker_consumption_result_maps_into_stable_worker_state_snapshots_correctly(
    client,
    db_session: Session,
) -> None:
    token = _login_as_super_admin(client, db_session)
    job_run_id = _prepare_handled_derived_job_run(client, db_session, token, mode="retry")

    dispatch_request = get_dispatch_request_projection(db_session, job_run_id=job_run_id)
    payload = build_queue_enqueue_payload(dispatch_request)
    worker_consumption = map_lifecycle_to_worker_consumption(
        map_redis_semantics_to_lifecycle_contracts(
            map_envelope_to_redis_semantics(RedisPlaceholderQueueSerializer().serialize(payload))
        )
    )
    timeline = map_worker_consumption_to_state_timeline(worker_consumption)

    assert timeline.snapshots[0].state == "pending"
    assert timeline.snapshots[1].state == "claimed"
    assert timeline.snapshots[3].state == "ack_pending"
    assert timeline.snapshots[4].state == "acked"
    assert timeline.snapshots[5].state == "redelivered"


